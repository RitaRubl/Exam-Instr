//Создать функцию, которая ищет наибольшее число в массиве. Написать пять модульных тестов для проверки работоспособности данной функции.
using System;

class Program
{
    // Функция поиска максимума
    static int FindMax(int[] arr)
    {
    // Инициализируем переменную max первым элементом массива.
    // Предполагаем, что первый элемент является максимальным на начальном этапе.
        int max = arr[0];
    // Начинаем цикл со второго элемента массива (индекс 1),
    // так как первый элемент уже сохранен в переменной max.
    // Цикл выполняется до последнего элемента массива (arr.Length - 1).
        for (int i = 1; i < arr.Length; i++)
        // На каждой итерации проверяем, является ли текущий элемент (arr[i])
        // больше текущего максимального значения (max).
        // Если текущий элемент больше, обновляем значение max.
            if (arr[i] > max) max = arr[i];
    // После завершения цикла возвращаем найденное максимальное значение.
    // К этому моменту переменная max содержит наибольшее число из всего массива.
        return max;
    }
    
    static void Main()
    {
        Console.WriteLine("Тесты FindMax:\n");
        
        // Тест 1
        Console.WriteLine($"Тест 1: {FindMax(new int[] { 1, 2, 3 })} (ожидается 3)");
        
        // Тест 2  
        Console.WriteLine($"Тест 2: {FindMax(new int[] { -1, -2, -3 })} (ожидается -1)");
        
        // Тест 3
        Console.WriteLine($"Тест 3: {FindMax(new int[] { 5 })} (ожидается 5)");
        
        // Тест 4
        Console.WriteLine($"Тест 4: {FindMax(new int[] { 4, 4, 4 })} (ожидается 4)");
        
        // Тест 5
        Console.WriteLine($"Тест 5: {FindMax(new int[] { 10, 0, -10 })} (ожидается 10)");
        
        Console.ReadKey();
    }
}
