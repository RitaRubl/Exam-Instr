//Создать функцию, для сложения двух чисел. Написать пять
//модульных тестов для проверки работоспособности данной функции.
// Подключение пространства имен System для использования базовых классов
using System;

class Program
{
    // Функция сложения двух целых чисел
    // Использует лямбда-выражение для краткой записи
    // Возвращает сумму параметров a и b
    public static int Add(int a, int b) => a + b;
    
    // Вспомогательная функция для тестирования (аналог Assert в юнит-тестах)
    // Сравнивает ожидаемое значение с фактическим результатом
    // Параметры:
    //   expected - ожидаемое значение теста
    //   actual - фактическое значение, полученное при выполнении
    //   testName - название теста для вывода информации
    static void AssertEqual(int expected, int actual, string testName)
    {
        // Проверяем, совпадает ли ожидаемое значение с фактическим
        if (expected == actual)
        {
            // Если значения совпадают - тест пройден
            // Устанавливаем зеленый цвет для успешного теста
            Console.ForegroundColor = ConsoleColor.Green;
            // Выводим сообщение об успехе с галочкой
            Console.WriteLine($"✓ {testName}: ПРОЙДЕН");
        }
        else
        {
            // Если значения не совпадают - тест провален
            // Устанавливаем красный цвет для неудачного теста
            Console.ForegroundColor = ConsoleColor.Red;
            // Выводим сообщение о провале с крестиком и деталями
            Console.WriteLine($"✗ {testName}: ПРОВАЛ (ожидалось {expected}, получено {actual})");
        }
        // Сбрасываем цвет консоли к стандартному
        Console.ResetColor();
    }
    
    // Главный метод программы - точка входа
    static void Main()
    {
        // Вывод заголовка тестирования
        Console.WriteLine("Модульные тесты для функции Add()\n");
        
        // Тест 1: Сложение двух положительных чисел
        // Проверяет базовый случай работы функции
        // Ожидаемый результат: 5 + 3 = 8
        AssertEqual(8, Add(5, 3), "Тест 1: Сложение положительных чисел");
        
        // Тест 2: Сложение двух отрицательных чисел
        // Проверяет работу функции с отрицательными значениями
        // Ожидаемый результат: (-4) + (-2) = -6
        AssertEqual(-6, Add(-4, -2), "Тест 2: Сложение отрицательных чисел");
        
        // Тест 3: Сложение положительного и отрицательного числа
        // Проверяет операцию вычитания через сложение
        // Ожидаемый результат: 10 + (-7) = 3
        AssertEqual(3, Add(10, -7), "Тест 3: Сложение разных знаков");
        
        // Тест 4: Сложение с нулем
        // Проверяет, что ноль является нейтральным элементом сложения
        // Ожидаемый результат: 0 + 15 = 15
        AssertEqual(15, Add(0, 15), "Тест 4: Сложение с нулем");
        
        // Тест 5: Сумма дает ноль
        // Проверяет сложение противоположных чисел
        // Ожидаемый результат: 100 + (-100) = 0
        AssertEqual(0, Add(100, -100), "Тест 5: Сумма дает ноль");
        
        // Вывод сообщения о завершении тестирования
        Console.WriteLine("\nТестирование завершено!");
        
        // Ожидание нажатия клавиши для завершения программы
        Console.ReadKey();
    }
}
