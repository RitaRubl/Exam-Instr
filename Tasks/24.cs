//Создать функцию, для определения количества специальных символов в строке. Написать пять модульных тестов для проверки работоспособности данной функции.
using System;

class Program
{
    // Функция для подсчета специальных символов в строке
    // Специальные символы: пробелы, знаки препинания, математические символы и т.д.
    static int CountSpecialChars(string str)
    {
        // Если строка null или пустая, возвращаем 0
        // Это защита от ошибок при работе с пустыми строками
        if (string.IsNullOrEmpty(str)) return 0;
        
        int count = 0; // Инициализируем счетчик
        
        // Перебираем каждый символ в строке
        foreach (char c in str)
        {
            // Char.IsLetterOrDigit(c) возвращает true, если символ - буква или цифра
            // Отрицание (!) дает true для всех остальных символов - спецсимволов
            if (!Char.IsLetterOrDigit(c))
                count++; // Увеличиваем счетчик для спецсимвола
        }
        
        return count; // Возвращаем общее количество
    }
    
    static void Main()
    {
        Console.WriteLine("Пять тестов CountSpecialChars:\n");
        
        // Тест 1: Только специальные символы
        // Строка содержит 5 спецсимволов: ! @ # $ %
        int test1 = CountSpecialChars("!@#$%");
        Console.WriteLine($"Тест 1: '!@#$%' -> {test1} (ожидается 5)");
        
        // Тест 2: Строка с русским текстом и пунктуацией
        // Строка содержит 3 спецсимвола: запятая, пробел, восклицательный знак
        int test2 = CountSpecialChars("Привет, мир!");
        Console.WriteLine($"Тест 2: 'Привет, мир!' -> {test2} (ожидается 3)");
        
        // Тест 3: Только буквы и цифры - без спецсимволов
        // Ожидаем 0, так как все символы - буквы или цифры
        int test3 = CountSpecialChars("Hello123");
        Console.WriteLine($"Тест 3: 'Hello123' -> {test3} (ожидается 0)");
        
        // Тест 4: Пустая строка - граничный случай
        // Должно вернуть 0
        int test4 = CountSpecialChars("");
        Console.WriteLine($"Тест 4: '' -> {test4} (ожидается 0)");
        
        // Тест 5: Смешанная строка с разными типами символов
        // Спецсимволы: #, пробел, >, пробел, ? (всего 5)
        int test5 = CountSpecialChars("C# > Java?");
        Console.WriteLine($"Тест 5: 'C# > Java?' -> {test5} (ожидается 5)");
        
        Console.WriteLine("\nНажмите любую клавишу...");
        Console.ReadKey();
    }
}
