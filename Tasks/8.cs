// Разработать метод, которому передается коллекция объектов типа ArrayList, а возвращается коллекция ArrayList, но уже без дубликатов. Необходимо использовать метод contains() интерфейса List.
// Подключение пространства имен System для использования базовых классов, таких как Console
using System;
// Подключение пространства имен System.Collections.Generic для работы с обобщенными коллекциями, такими как List<T>
using System.Collections.Generic;

class Program
{
    /// <summary>
    /// Главный метод программы - точка входа
    /// </summary>
    static void Main()
    {
        // Создание исходного списка строк с дубликатами
        List<string> list = new List<string> { "A", "B", "A", "C", "B", "D" };
        
        // Вызов метода RemoveDuplicates для удаления дубликатов из списка
        List<string> unique = RemoveDuplicates(list);
        
        // Вывод заголовка результатов
        Console.WriteLine("Результат:");
        
        // Цикл foreach для перебора и вывода всех элементов результирующего списка
        foreach (string item in unique)
            Console.WriteLine(item); // Вывод каждого уникального элемента на новой строке
    }
    
    /// <summary>
    /// Обобщенный метод для удаления дубликатов из списка
    /// </summary>
    /// <typeparam name="T">Тип элементов в списке (определяется автоматически при вызове)</typeparam>
    /// <param name="list">Исходный список, из которого нужно удалить дубликаты</param>
    /// <returns>Новый список, содержащий только уникальные элементы из исходного списка</returns>
    public static List<T> RemoveDuplicates<T>(List<T> list)
    {
        // Создание нового пустого списка для хранения результата
        List<T> result = new List<T>();
        
        // Цикл foreach для перебора всех элементов исходного списка
        foreach (T item in list)
        {
            // Проверка с помощью метода Contains(): если элемент уже есть в результирующем списке
            // Метод Contains() выполняет линейный поиск в списке
            if (!result.Contains(item))
            {
                // Если элемент не найден в результирующем списке, добавляем его
                result.Add(item);
            }
            // Если элемент уже есть в результате, он пропускается (дубликат не добавляется)
        }
        
        // Возврат результирующего списка без дубликатов
        return result;
    }
}
